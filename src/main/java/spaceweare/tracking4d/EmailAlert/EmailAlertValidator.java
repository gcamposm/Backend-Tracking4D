package spaceweare.tracking4d.EmailAlert;

public class EmailAlertValidator {
    private static EmailAlertValidator instance;

    private EmailAlertValidator(){

    }
    public static EmailAlertValidator getInstance(){
        if(instance == null){
            instance = new EmailAlertValidator();
        }
        return instance;
    }


    public boolean isValidEmailAddress(String email) {
        String ePattern = "^[a-zA-Z0-9.!#$%&'*+/=?^_`{|}~-]+@((\\[[0-9]{1,3}\\.[0-9]{1,3}\\.[0-9]{1,3}\\.[0-9]{1,3}\\])|(([a-zA-Z\\-0-9]+\\.)+[a-zA-Z]{2,}))$";
        java.util.regex.Pattern p = java.util.regex.Pattern.compile(ePattern);
        java.util.regex.Matcher m = p.matcher(email);
        return m.matches();
    }
}
